#include "libft.h"

char	*base8(unsigned long long n)
{
	char	*str;
	int		i;

	i = 0;
	str = malloc(30);
	if (!n)
		return ("0");
	while (i > -1 && n)
	{
		str[i++] = '0' + (n & 7);
		n = n >> 3;
	}
	str[i] = '\0';
	str = rev_str(str);
	return (str);
}
